    protected $fillable = [
        'name',
        'email',
        'phone',
        'password',
        'remember_token',
        'referral_code',
        'email_verified_token',
        'email_verified_at',
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var array<int, string>
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    /**
     * The attributes that should be cast.
     *
     * @var array<string, string>
     */
